# Rewrite the code using ELIF statements

Score = int(input("Give me the score of your grade. "))

if Score >= 90:
    print("You got an A!")
elif Score >= 80:
    print("You got a B.")
elif Score >= 70:
    print("You got a C")
elif Score >= 60:
    print("You got a D")
else:
    print("YOu got an F and you a retard.")